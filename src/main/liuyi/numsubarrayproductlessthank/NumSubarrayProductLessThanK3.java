	public int numSubarrayProductLessThanK(int[] nums, int k) {
        int result = 0;
        if (nums.length == 0 || k < 2) {
			return result;
		}
        // 外圈循环,从第几个数开始
        for (int i = 0; i < nums.length; i++) {
            int p = 1;
            // 乘到第几个数
            int j = i;
            for (; j < nums.length && p < k; j++) {
                p *= nums[j];
            }
            if (p < k) {
                result += j - i;
            } else {
                result += j - i - 1;
            }

		}
		return result;
        
    }
