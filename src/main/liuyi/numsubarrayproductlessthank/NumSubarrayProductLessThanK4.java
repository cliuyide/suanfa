	public int numSubarrayProductLessThanK(int[] nums, int k) {
        int result = 0;
        if (nums.length == 0 || k < 2) {
			return result;
		}
        // 外圈循环,从第几个数开始
        int p = 1;
        int left=0;
        for (int i = 0; i < nums.length; i++) {
        	p*=nums[i];
        	while(p>=k){
        		p/=nums[left];
        		left++;
        	}
        	result+=(i-left + 1);
		}
		return result;
        
    }
